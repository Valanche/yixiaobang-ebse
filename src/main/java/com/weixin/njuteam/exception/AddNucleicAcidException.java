package com.weixin.njuteam.exception;

/**
 * 添加核酸信息异常
 *
 * @author Zyi
 */
public class AddNucleicAcidException extends RuntimeException {

	public AddNucleicAcidException() {
		super();
	}

	public AddNucleicAcidException(Throwable cause) {
		super(cause);
	}

	public AddNucleicAcidException(String message, Throwable cause) {
		super(message, cause);
	}

	public AddNucleicAcidException(String message) {
		super(message);
	}
}
